//Arithmetic Operators.
let x = 10
let y = 20
console.log(x+y);
console.log(x-y);
console.log(x*y);
console.log(x/y);
console.log(y%x);
//Relational Operators.
console.log(x>y);
console.log(x<y);
console.log(x==y);
console.log(x===y);
console.log(x=!y);
console.log(x==!y);
console.log(++y);
console.log(--y);
//Logical Operators.
console.log(10>5 && 5<10);
console.log(10<5 && 5<10);
console.log(10>5 || 5<10);
console.log(10<5 || 5>10);
//Bitwise Operator.
console.log(14 & 9);//convert it into 32 bit and check every bit
console.log(14 | 9);