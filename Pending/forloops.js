//Incremeting.
for(let i=0;i<4;i++){
    console.log(`i:${i}`);
    for(let j=0;j<4;j++){
        console.log(` j:${j}`);
        
    }
    console.log("-------------");
}

console.log("---------------");
//-----------------------------
//Decremeting.
for(let i=4;i>0;i--){
    console.log(`i:${i}`);
    for(let j=0;j<i;j++){
        console.log(` j:${j}`);
        
    }
    console.log("-------------");

}